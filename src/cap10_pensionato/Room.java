package cap10_pensionato;

public class Room {
	private String name;
	private String email;
	
	public Room(String nome, String email) {
		this.name = nome;
		this.email = email;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	
	

}
